
public class Character_toUpperCase_and_toLowerCase {

	public static void main(String[] args) {
		//Character.toUpperCase(): 명시된 문자를 대문자로 반환
		//Character.toLowerCase(): 명시된 문자가 소문자로 r반환
				
		System.out.println(Character.toUpperCase('t'));
				
		System.out.println(Character.toLowerCase('T'));

	}

}
